PI = 3.14
print("This is a constant file")

print(4* PI)
print(round(4 ** PI,2)) # This is a Exponentiation
print(4 / PI) # This is a Division
print(4 // PI) # This is a Integer Division
print(4 % PI) # This is Modulus
print(4 + PI) # This is Addition
print(4 - PI) # This is Subtraction
print(4 * PI) # This is Multiplication
