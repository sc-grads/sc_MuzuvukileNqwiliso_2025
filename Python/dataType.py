# Numeric types
# int, float, complex
number = 10
print(type(number))  #  This is going to <class 'int'>
float_number = 10.5
print(type(float_number))  # This is going to <class 'float'>

# Boolean type
print(type(True))  # This is going to <class 'bool'>
print(type(False))  # This is going to <class 'bool'>
# String type
print(type("Hello"))  # This is going to <class 'str'>
print(type('Hello'))  # This is going to <class 'str'>
# str
# list, tuple, range
numbers = [1, 2, 3, 4, 5]
print(type(numbers))  # This is going to <class 'list'>
numbers = (1, 2, 3, 4, 5)
print(type(numbers))  # This is going to <class 'tuple'>
numbers = range(1, 10)
